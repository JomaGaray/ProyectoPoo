    private void jButtonVerMateriaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonVerMateriaActionPerformed
        // TODO add your handling code here:
        
        if (this.jTableMaterias.getSelectedRowCount()> 0  && this.jTableMaterias.getSelectedColumnCount()> 0 ){
            
            int fila = this.jTableMaterias.getSelectedRow();
            int column = this.jTableMaterias.getSelectedColumn();
            
            if (this.jTableMaterias.getValueAt(fila, column) != null ){
                
                String materia = (String) this.jTableMaterias.getValueAt(fila, column);
                Materia m = this.plan.obtenerMateriaPorNombre(materia);
               
                PantallaDetalleMateria pantallaDetalle = new PantallaDetalleMateria(m.getNombre(), m.getSemestre(), m.getCorrelativaAnterior() != null ? m.getCorrelativaAnterior().getNombre() : "no tiene", m.getProfesor() != null ? m.getProfesor() : "no tiene");
    
            }else{
            
                JOptionPane.showMessageDialog(rootPane, "Seleccione alguna materia valida", "Aviso", 2);
            }
        }else{
            
            JOptionPane.showMessageDialog(rootPane, "Seleccione alguna materia", "Aviso", 2);
        }
    }//GEN-LAST:event_jButtonVerMateriaActionPerformed

