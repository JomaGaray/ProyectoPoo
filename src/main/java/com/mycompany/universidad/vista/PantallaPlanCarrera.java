    private void cargarMaterias(PlanEstudio plan){
        
        ArrayList<ArrayList <Materia> > materias = plan.getCuatrimestres();
        
        
        System.out.println("Entramos en el metodo para completar la tabla...");
        
        System.out.println("tamaño de lista materias " +  materias.size() );
        
        
        //recibo una lista de materias para mostrar
        for (int i = 1; i < materias.size(); i++) {
            
            for (int j = 0; j < materias.get(i).size(); j++) {
                
                System.out.println("tamaño de lista materias del cuat " +  i + ": " + materias.get(i ).size());
                
                
                String nombreMateria = materias.get(i).get(j).getNombre();
                    
                //seteo el nombre de la materia en la tabla
                jTableMaterias.getModel().setValueAt(nombreMateria, j, i - 1);
                
            }
        }
    }
        
    
    
    
    
